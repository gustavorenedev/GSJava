package Epoca;

public class Epoca {
	private String epoca;
	private String caracteristicaEpoca;
	
	public Epoca(String epoca, String caracteristicaEpoca) {
		this.epoca = epoca;
		this.caracteristicaEpoca = caracteristicaEpoca;
	}

	public String getEpoca() {
		return epoca;
	}

	public void setEpoca(String epoca) {
		this.epoca = epoca;
	}

	public String getCaracteristicaEpoca() {
		return caracteristicaEpoca;
	}

	public void setCaracteristicaEpoca(String caracteristicaEpoca) {
		this.caracteristicaEpoca = caracteristicaEpoca;
	}
	
	
}
